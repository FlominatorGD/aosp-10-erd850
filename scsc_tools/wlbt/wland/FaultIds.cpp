static dujeonglee::basiclibrary::map::plain::UnorderedMap<int, std::string> faultIdMap(0xFFFF, "UNKNOWN_MESSAGE");

FaultInd *FaultInd::mInstance = new FaultInd;

FaultInd *FaultInd::getInstance()
{
    return mInstance;
}


const char* FaultInd::getFaultMessage(int id) 
{
    std::string *ret = faultIdMap.FindPtr(id);
    if(ret == NULL)
    {
        return "Can't found Fault Msg";
    }
    return ret->c_str();
}

FaultInd::FaultInd()
{
    for(unsigned int i = 0 ; i < sizeof(FaultId_MsgString) / sizeof(FaultMsgString); i++)
    {
        faultIdMap.Insert(std::make_pair(FaultId_MsgString[i].fault_id, std::string(FaultId_MsgString[i].fault_msg)));
    }
};

FaultInd::~FaultInd()
{
    faultIdMap.Clear();
}
